    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblUserAccounts.getSelectedRow();
        if (selectedRow>=0) {
            Person person = (Person) tblUserAccounts.getValueAt(selectedRow,0);
            business.getPersonDirectory().deletePerson(person);
            business.getUserAccountDirectory().deleteUser(person.getUserAccount());
            JOptionPane.showMessageDialog(null,"User has been deleted");
            populateTable();
        }
        else{
            JOptionPane.showMessageDialog(null,"select any row");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

