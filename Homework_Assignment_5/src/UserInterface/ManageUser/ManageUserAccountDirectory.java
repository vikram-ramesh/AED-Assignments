    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblUserAccounts.getModel();
        dtm.setRowCount(0);
        JPasswordField pwf = new JPasswordField();
        DefaultCellEditor editor = new DefaultCellEditor(pwf);
        tblUserAccounts.getColumnModel().getColumn(1).setCellEditor(editor);
        for(Person person: business.getPersonDirectory().getPersonList()) {
            Object row[] = new Object[4];
            
            if (person.getUserAccount()==null) {
                continue;
            }
            else{
                row[0] = person;
                row[1] = person.getUserAccount().getUserName();
                row[2] = person.getUserAccount().getPassword();
                row[3] = person.getUserAccount().getRole();
            }
            dtm.addRow(row);
        }
    }
