    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // TODO add your handling code here:
        String username = txtUsername.getText();
        String password = String.copyValueOf(pwdField.getPassword());

        UserAccount ua =business.getUserAccountDirectory().isValidUser(username,password);

        if(ua!=null){
            if((ua.getRole().equals("System Admin"))&&(ua.getAccountStatus()==true)){
                AdminWorkAreaJPanel panel = new AdminWorkAreaJPanel(userProcessContainer, business);
                userProcessContainer.add("AdminWorkAreaJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else if(ua.getRole().equals("Supplier")&(ua.getAccountStatus()==true)){
                SupplierWorkAreaJPanel panel = new SupplierWorkAreaJPanel(userProcessContainer,ua.getSupplier(), business.getMarketList());
                userProcessContainer.add("SupplierWorkAreaJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else if(ua.getRole().equals("SalesPerson")&(ua.getAccountStatus()==true)){
                SalesPersonJPanel panel = new SalesPersonJPanel(userProcessContainer,ua.getSalesPerson(),business);
                userProcessContainer.add("SalesPersonWorkAreaJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
            else if(ua.getRole().equals("Customer")&(ua.getAccountStatus()==true)){
                CustomerWorkAreaJPanel panel = new CustomerWorkAreaJPanel(userProcessContainer,ua.getCustomer(),business.getMasterOrderCatalog());
                userProcessContainer.add("CustomerWorkAreaJPanel", panel);
                CardLayout layout = (CardLayout) userProcessContainer.getLayout();
                layout.next(userProcessContainer);
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Invalid Username or Password !");
        }
    }//GEN-LAST:event_loginJButtonActionPerformed


