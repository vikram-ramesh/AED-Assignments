    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String supplierName = txtSupplierName.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        int j=0;
        if (!supplierName.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(null, "Supplier name should contain only letters", "Info", JOptionPane.WARNING_MESSAGE);
            j++;
        }
        else if(!username.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(null, "Username should contain only letters", "Info", JOptionPane.WARNING_MESSAGE);
            j++;
        }
        else if(!password.matches("^[a-zA-Z0-9]+$")) {
            JOptionPane.showMessageDialog(null, "Password can contain only letters", "Info", JOptionPane.WARNING_MESSAGE);
            j++;
        }
        else{}
        
        if (j==0){
            
            Supplier supplier = supplierDirectory.addSupplier();
            supplier.setSupplierName(txtSupplierName.getText());
            UserAccount user = userAccountDirectory.addUserAccount();
            user.setAccountStatus(true);
            user.setSupplier(supplier);
            user.setUsername(username);
            user.setPassword(password);
            user.setRole("Supplier");
            JOptionPane.showMessageDialog(null, "Supplier added successfully!!");
        
        }
        
    }//GEN-LAST:event_btnAddActionPerformed

