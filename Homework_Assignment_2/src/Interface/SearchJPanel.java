    private void deleteJBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteJBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = airlinesTable.getSelectedRow();

        if (selectedRow>=0) {
            Airplanes vs = (Airplanes)airlinesTable.getValueAt(selectedRow, 0);
            airplanesCatalog.deleteAirplanes(vs);
            JOptionPane.showMessageDialog(null,"Airplane has been deleted");
            populateTable();
        }
        else{
            JOptionPane.showMessageDialog(null,"select any row");
        }
    }//GEN-LAST:event_deleteJBtnActionPerformed

