    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = airlinesTable.getSelectedRow();
        if (selectedRow>=0) {
            Airplanes vs = (Airplanes)airlinesTable.getValueAt(selectedRow, 0);
            airplanesCatalog.deleteAirplanes(vs);
            
            Date availableDate = null;
            String serialNo = serialNoTxt.getText();
            String modelNo = modelNoTxt.getText();
            String manufacturer = manufacturedByTxt.getText();
            int year = Integer.parseInt(yearTxt.getText());
            int seats = Integer.parseInt(numberOfSeatsTxt.getText());
            String maintainence = maintainceCertificateTxt.getText();
            String airport = airportTxt.getText();
            try {
                availableDate = new SimpleDateFormat("dd-mm-yyyy").parse(availableDateTxt.getText());
            } catch (ParseException ex) {
                Logger.getLogger(CreateJPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Enter the date in proper format");
            return;
            }
            Date catalogueUpdate = new Date();
            Airplanes air = airplanesCatalog.addAirplanes();
            air.setSerialNo(serialNo);
            air.setModelNo(modelNo);
            air.setManufacturer(manufacturer);
            air.setYearOfManufacture(year);
            air.setNumberOfSeats(seats);
            air.setMaintaineneceCertificate(maintainence);
            //air.setAvailabilityDate(availableDate);
            air.setAirport(airport);
            air.setDepartureDate(availableDate);
            air.setCatalogUpdatedDate(catalogueUpdate);
            JOptionPane.showMessageDialog(null, "Airplanes updated Successfully");
            populateTable();
        }
        serialNoTxt.setText("");
        modelNoTxt.setText("");
        manufacturedByTxt.setText("");
        yearTxt.setText("");
        numberOfSeatsTxt.setText("");
        maintainceCertificateTxt.setText("");
        airportTxt.setText("");
        availableDateTxt.setText("");
       
    }//GEN-LAST:event_updateBtnActionPerformed

    private void yearTxtKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_yearTxtKeyTyped
        // TODO add your handling code here:
        char year= evt.getKeyChar();
        if (!(Character.isDigit(year) || (year==KeyEvent.VK_BACK_SPACE) || (year==KeyEvent.VK_DELETE))) {
            evt.consume();
        }
    }//GEN-LAST:event_yearTxtKeyTyped


