    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)airlinesTable.getModel();
        dtm.setRowCount(0);
        SimpleDateFormat date = new SimpleDateFormat("dd-mm-yyyy");
        
        for(Airplanes as: airplanesCatalog.getAirplanesCatalog()){
            Object row[]= new Object[8]; 
            row[0]=as;
            row[1] = as.getModelNo();
            row[2] = as.getManufacturer();
            row[3] = as.getYearOfManufacture();
            row[4] = as.getNumberOfSeats();
            row[5] = as.getMaintaineneceCertificate();
            row[6] = as.getAirport();
            String availDate = date.format(as.getDepartureDate());
            row[7] = availDate;
            dtm.addRow(row);
        }
    }

