    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        String airlinerName = txtAirlinerName.getText();
        String airlinerId = txtAirlinerId.getText();
        String address = txtAirlinerAddress.getText();
        int noOfFlights;
        try{
            noOfFlights = Integer.parseInt(txtNoOfFlights.getText());
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "All fields are mandatory");
            return;
        }
        if (airlinerName.equals("") || airlinerId.equals("") || address.equals("") ) {
            JOptionPane.showMessageDialog(null, "All fields are mandatory");
            return;
        }
        else{
            //int noOfFlights = Integer.parseInt(txtNoOfFlights.getText());
            airline = travelAgency.getAirlinerDirectory().addAirliner();
            airline.setAirlinerName(airlinerName);
            airline.setAirlinerId(airlinerId);
            airline.setAirlinerAddress(address);
            airline.setNoOfFlights(noOfFlights);
            
            JOptionPane.showMessageDialog(null, "Airliner created successfully");
        }        
    }//GEN-LAST:event_btnCreateActionPerformed

